@Composable
fun SwiperCompoent(viewModel: MainViewModel, modifier:Modifier = Modifier) {
    Box(modifier = modifier.fillMaxWidth().fillMaxHeight(0.24f)) {
        val pagerState = rememberPagerState(pageCount = { viewModel.swiperPager.size })
        val pagerIsDragged by pagerState.interactionSource.collectIsDraggedAsState()

        val pageInteractionSource = remember { MutableInteractionSource() }
        val pageIsPressed by pageInteractionSource.collectIsPressedAsState()

        // Stop auto-advancing when pager is dragged or one of the pages is pressed
        val autoAdvance = !pagerIsDragged && !pageIsPressed
        if (autoAdvance) {
            LaunchedEffect(pagerState, pageInteractionSource) {
                while (true) {
                    delay(2000)
                    val nextPage = (pagerState.currentPage + 1) % viewModel.swiperPager.size
                    pagerState.animateScrollToPage(nextPage)
                }
            }
        }

        HorizontalPager(state = pagerState,modifier=modifier.fillMaxSize()) { page ->
            AsyncImage(
                model = viewModel.swiperPager[page].imgUrl,
                contentDescription = "",
                modifier = modifier
                    .fillMaxWidth()
                    .aspectRatio(7 / 3f)
                    .padding(horizontal = 16.dp)
                    .clip(RoundedCornerShape(8.dp)),
                contentScale = ContentScale.Crop
            )
        }
        PagerIndicator(viewModel.swiperPager.size, pagerState.currentPage)
    }
}

@Composable
fun PagerIndicator(pageCount: Int, currentPageIndex: Int, modifier: Modifier = Modifier) {
    Box(modifier = Modifier.fillMaxSize()) {
        Row(
            modifier = Modifier
                .wrapContentHeight()
                .fillMaxWidth()
                .align(Alignment.BottomCenter)
                .padding(bottom = 8.dp),
            horizontalArrangement = Arrangement.Center
        ) {
            repeat(pageCount) { iteration ->
                val color = if (currentPageIndex == iteration) Color.Cyan else Color.LightGray
                Box(
                    modifier = modifier
                        .padding(2.dp)
                        .clip(CircleShape)
                        .background(color)
                        .size(8.dp)
                )
            }
        }
    }
}